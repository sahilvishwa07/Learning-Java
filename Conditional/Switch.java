package Conditional;

public class Switch {
    public static void main(String[] args) {
        
    
    int n = 6;
    switch (n){
        case 1 : System.out.println("samosa");
                break;
        case 2 : System.out.println("itli");
                break;
        case 3 : System.out.println("panner");
                break;
        default : System.out.println("jaag jao");
    }
    }
}
