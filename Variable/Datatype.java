package Variable;
public class Datatype {
    public static void main(String[] args) {
        // PRIMITIVE DATA TYPE
        // range 256 SIZE=1BYTE[ -128 To 127]
        byte b = 8; 
        System.out.println(b);
        //  single character = 2 byte
        char ch = 'a';
        System.out.println(ch);
        // store only true/false = 1 byte
        boolean var = true;
        System.out.println(var);
        // float = 4 byte
        float price = 0.5F;
        System.out.println(price);
        // long = 8 byte
        // double = 8 byte
        //  shortt = 2 byte
        short n =240;
        System.out.println(n);
        
    }
}
